void	ft_rev_int_tab(int *tab, int size)
{
	int	i;	
	int	j;
	int	swap;

	i = 0;
	j = size;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab [i] = tab [j - 1];
		tab [j - 1] = swap;
		i++;
		j--;
	}
}
